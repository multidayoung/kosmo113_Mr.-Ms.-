package kr.co.animal.vo;

public class PointVO {
	private int r_num;
	private int point_num;
	private String member_id; 
	private String point_cate; 
	private int point;
	private String point_cont; 
	private String point_date;
	
	
	public int getR_num() {
		return r_num;
	}
	public void setR_num(int r_num) {
		this.r_num = r_num;
	}
	public int getPoint_num() {
		return point_num;
	}
	public void setPoint_num(int point_num) {
		this.point_num = point_num;
	}
	public String getMember_id() {
		return member_id;
	}
	public void setMember_id(String member_id) {
		this.member_id = member_id;
	}
	public String getPoint_cate() {
		return point_cate;
	}
	public void setPoint_cate(String point_cate) {
		this.point_cate = point_cate;
	}
	public int getPoint() {
		return point;
	}
	public void setPoint(int point) {
		this.point = point;
	}
	public String getPoint_cont() {
		return point_cont;
	}
	public void setPoint_cont(String point_cont) {
		this.point_cont = point_cont;
	}
	public String getPoint_date() {
		return point_date;
	}
	public void setPoint_date(String point_date) {
		this.point_date = point_date;
	} 

	
}
