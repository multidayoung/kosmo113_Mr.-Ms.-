package kr.co.animal.vo;

public class FriendsVO {
	private String friends_userid;
	private String friends_status;
	private String friends_friendid;
	
	public String getFriends_userid() {
		return friends_userid;
	}
	public void setFriends_userid(String friends_userid) {
		this.friends_userid = friends_userid;
	}
	public String getFriends_status() {
		return friends_status;
	}
	public void setFriends_status(String friends_status) {
		this.friends_status = friends_status;
	}
	public String getFriends_friendid() {
		return friends_friendid;
	}
	public void setFriends_friendid(String friends_friendid) {
		this.friends_friendid = friends_friendid;
	}
	
}
